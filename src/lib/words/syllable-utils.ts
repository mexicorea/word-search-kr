// 단어에서 음절 분리
export function splitIntoSyllables(word: string): string[] {
  return [...word]
}

// 한글 음절인지 확인
export function isKoreanSyllable(char: string): boolean {
  const code = char.charCodeAt(0)
  return code >= 0xAC00 && code <= 0xD7A3
}

// 랜덤 한글 음절 생성
export function randomKoreanSyllable(): string {
  const cho = Math.floor(Math.random() * CHOSEONG.length)
  const jung = Math.floor(Math.random() * JUNGSEONG.length)
  // 받침 없는 경우를 더 많이 생성 (자연스러운 한글)
  const jong = Math.random() < 0.4 ? Math.floor(Math.random() * JONGSEONG.length) : 0
  const code = 0xAC00 + (cho * 21 + jung) * 28 + jong
  return String.fromCharCode(code)
}

// 자주 쓰이는 음절에서 랜덤 선택
export function randomCommonSyllable(): string {
  return COMMON_SYLLABLES[Math.floor(Math.random() * COMMON_SYLLABLES.length)]
}
